#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t i;
	size_t j;

	i = 0;
	j = 0;
	while (j <  n / 8)
	{
		((long long *)s)[i++] = 0;
		j++;
	}
	n -= j * 8;
	j = 0;
	while (j <  n / 4)
	{
		((int *)s)[i++] = 0;
		j++;
	}
	n -= j * 4;
	j = 0;
	while (j <  n / 2)
	{
		((short *)s)[i++] = 0;
		j++;
	}
	n -= j * 2;
	if (n == 1)
		((char *)s)[i] = 0;

}